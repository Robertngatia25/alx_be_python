# future_age_calculator

#current age of the inputer

current_age = int(input("How old are you? "))

# Calculate the number of years between the assumed current year (2023) and the future year (2050)
years_to_future = 2050 - 2023

# Calculate the age in 2050
future_age = current_age + years_to_future

# Print the result
print(f"In 2050, you will be {future_age} years old.")